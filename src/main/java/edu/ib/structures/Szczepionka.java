package edu.ib.structures;

public class Szczepionka {

    private String nazwa;
    private int id;

    public Szczepionka(String nazwa, int id) {
        this.nazwa = nazwa;
        this.id = id;
    }

    public String getNazwa() {
        return nazwa;
    }

    public int getId() {
        return id;
    }



}
